namespace Folio.CORE.Enums
{
    /// <summary>
    /// LiteraryGenres - defines available book genre classifications
    /// Used to categorize books and filter by genre in searches
    /// </summary>
    public enum LiteraryGenres
    {
        /// <summary>Fiction - imaginative narrative stories and novels</summary>
        Fiction = 0,

        /// <summary>NonFiction - factual, informational, and documentary works</summary>
        NonFiction = 1,

        /// <summary>Fantasy - imaginative worlds with magical or supernatural elements</summary>
        Fantasy = 2,

        /// <summary>SciFi - science fiction with futuristic or technological themes</summary>
        SciFi = 3,

        /// <summary>Horror - works designed to frighten or unsettle readers</summary>
        Horror = 4,

        /// <summary>Romance - stories centered on romantic relationships and emotions</summary>
        Romance = 5,

        /// <summary>Thriller - suspenseful stories with tension and dramatic plot twists</summary>
        Thriller = 6,

        /// <summary>Biography - life stories and biographical accounts of real people</summary>
        Biography = 7,

        /// <summary>History - historical accounts and narratives of past events</summary>
        History = 8,

        /// <summary>Poetry - poetic works with verse, rhythm, and literary devices</summary>
        Poetry = 9,

        /// <summary>Other - books that don't fit standard genre classifications</summary>
        Other = 10
    }
}
